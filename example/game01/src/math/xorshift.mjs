// 毎フレーム色を変更する等、偏っていても全く問題ない用途のみに使う高速乱数
// (偏りが結構あるので要注意)
let x = 123456789, y = 362436069, z = 521288629, w = 88675123, t;
const max = 2**32;
export const xorshift = () => {
  t = x^(x<<11);
  x = y;
  y = z;
  z = w;
  w = (w^(w>>>19))^(t^(t>>>8));
  //w = (w^(w>>19))^(t^(t>>8));
  return 0.5 + w/max;
};
